    public void LeerDatos_usuarios() {
    try {
        java.io.File archivo = new java.io.File(rutaUsuarios);
        if (archivo.exists()) {
            java.io.FileInputStream fis = new java.io.FileInputStream(archivo);
            java.io.ObjectInputStream ois = new java.io.ObjectInputStream(fis);
            estructura_usuarios = (ArrayList<Usuario>) ois.readObject();
            ois.close(); fis.close();
        }
    } catch (Exception e) { System.out.println("Error usuarios: " + e.getMessage()); }
}

