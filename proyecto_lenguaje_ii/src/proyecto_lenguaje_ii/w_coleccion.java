    private void btnGrabarColActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarColActionPerformed
        // TODO add your handling code here:
        // Validación dentro del botón Grabar
        if (cmbFiguritas.getSelectedIndex() == 0) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                    "Por favor, selecciona una figurita válida de la lista.", 
                    "Campo Vacío", 
                    javax.swing.JOptionPane.WARNING_MESSAGE);
        return;// Frena el guardado
        }
        
        try {
            
        // 2. Capturamos los datos limpios de los combos
        String usuarioSeleccionado = cmbUsuarios.getSelectedItem().toString().split(" - ")[0];
        String figuritaSeleccionada = cmbFiguritas.getSelectedItem().toString().split(" - ")[0];
        String estadoSeleccionado = cmbEstado.getSelectedItem().toString();
        
        // Generamos un ID automático para este registro (ej: COL-1718293)
        String idAutomatico = "COL-" + System.currentTimeMillis(); 
        String cantidadPorDefecto = "1";

        // 3. Creamos el objeto usando el constructor de tus compañeros (imagen image_9f0145.png)
        ColeccionFigurita nuevaColeccion = new ColeccionFigurita(
            idAutomatico, 
            usuarioSeleccionado, 
            figuritaSeleccionada, 
            estadoSeleccionado, 
            cantidadPorDefecto
        );

        // 4. Agregamos a la lista de la RAM
        estructura_coleccion.add(nuevaColeccion);

        // 5. Guardamos en el archivo (Persistencia)
        java.io.File archivo = new java.io.File(rutaColeccion);
        java.io.FileOutputStream fos = new java.io.FileOutputStream(archivo);
        java.io.ObjectOutputStream oos = new java.io.ObjectOutputStream(fos);
        oos.writeObject(estructura_coleccion);
        oos.close();
        fos.close();

        // 6. Refrescamos la tabla visual
        MostrarDatos_coleccion();

        // 7. Mensaje de éxito
        javax.swing.JOptionPane.showMessageDialog(this, "¡Figurita registrada en tu colección con éxito!");
        cmbFiguritas.setSelectedIndex(0); 

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error al guardar: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_btnGrabarColActionPerformed

